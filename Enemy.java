public class Enemy{
	String name;
	int attack;
	int defense;
	int dexterity;
	int intelligence;
	int hp;
	int mana;
	
	public Enemy( String name, int attack, int defense, int dexterity, int intelligence, int hp, int mana){
		this.name = name;
		this.attack = attack;
		this.defense = defense;	
		this.dexterity = dexterity;
		this.intelligence = intelligence;
		this.hp =hp;
		this.mana =mana;
	}
}