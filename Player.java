public class Player{

	String name;
	String job;
	int attack;
	int defense;
	int dexterity;
	int intelligence;
	int hp;
	int mana;
	
	public Player( String name, String job, int attack, int defense, int dexterity, int intelligence, int hp, int mana){
		this.name = name;
		this.job = job;
		this.attack = attack;
		this.defense = defense;	
		this.dexterity = dexterity;
		this.intelligence = intelligence;
		this.hp =hp;
		this.mana =mana;
		
	}
}
